// ///////////////////////////////////////////////////////////////////// Imports
// -----------------------------------------------------------------------------
import { ref } from '#imports'

// ////////////////////////////////////////////////////////////////////// Export
// -----------------------------------------------------------------------------
export const useZeroStore = defineStore('zero', () => {
  // ===================================================================== state
  const clipboard = ref(false)

  // =================================================================== actions

  /**
   * @method setClipboard
   */

  const setClipboard = text => {
    clipboard.value = text
  }

  // ==================================================================== return
  return {
    // ----- state
    clipboard,
    // ----- actions
    setClipboard
  }
})
